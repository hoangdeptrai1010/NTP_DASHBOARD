CREATE TABLE APP_ROLES (
ROLE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
ROLE_CODE VARCHAR2(50) NOT NULL UNIQUE,
ROLE_NAME VARCHAR2(100) NOT NULL,
DESCRIPTION VARCHAR2(255),
CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE APP_USERS (
USER_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
USERNAME VARCHAR2(50) NOT NULL UNIQUE,
PASSWORD_HASH VARCHAR2(255) NOT NULL,
FULLNAME VARCHAR2(100) NOT NULL,
EMAIL VARCHAR2(254) UNIQUE,
PHONE VARCHAR2(20),
ROLE_ID NUMBER NOT NULL,
IDKHOAPHONG NUMBER,
IS_ACTIVE NUMBER(1) DEFAULT 1,
CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,

CONSTRAINT FK_USER_ROLE FOREIGN KEY (ROLE_ID)
    REFERENCES APP_ROLES(ROLE_ID),

CONSTRAINT FK_USER_KHOA FOREIGN KEY (IDKHOAPHONG)
    REFERENCES DMKHOAPHONGBV(IDKHOAPHONG)
);

CREATE TABLE BACSI (
DOCTOR_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
USER_ID NUMBER NOT NULL UNIQUE,
SPECIALIZATION VARCHAR2(100),
LICENSE_NUMBER VARCHAR2(50) UNIQUE,
LICENSE_DATE DATE,
EXPERIENCE_YEARS NUMBER,
CERTIFICATION VARCHAR2(255),
DEPARTMENT_ID NUMBER,

CONSTRAINT FK_BACSI_USER FOREIGN KEY (USER_ID)
    REFERENCES APP_USERS(USER_ID) ON DELETE CASCADE
);

CREATE TABLE TRUONGKHOA (
DEPT_HEAD_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
USER_ID NUMBER NOT NULL UNIQUE,
DEPARTMENT_ID NUMBER NOT NULL,
APPOINTMENT_DATE DATE,
APPOINTMENT_LETTER VARCHAR2(100),
SUPERVISED_DOCTORS NUMBER DEFAULT 0,

CONSTRAINT FK_TRUONGKHOA_USER FOREIGN KEY (USER_ID)
    REFERENCES APP_USERS(USER_ID) ON DELETE CASCADE,

CONSTRAINT UK_TRUONGKHOA UNIQUE (DEPARTMENT_ID)
);

CREATE TABLE GIAMDOC (
DIRECTOR_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
USER_ID NUMBER NOT NULL UNIQUE,
TITLE VARCHAR2(100),
APPOINTMENT_DATE DATE,
AUTHORITY_LEVEL NUMBER DEFAULT 1,
SIGNATURE VARCHAR2(255),

CONSTRAINT FK_GIAMDOC_USER FOREIGN KEY (USER_ID)
    REFERENCES APP_USERS(USER_ID) ON DELETE CASCADE
);

CREATE TABLE QUYEN (
PERMISSION_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
ROLE_ID NUMBER NOT NULL,
RESOURCE VARCHAR2(100) NOT NULL,
ACTION VARCHAR2(50) NOT NULL,
CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,

CONSTRAINT FK_QUYEN_ROLE FOREIGN KEY (ROLE_ID)
    REFERENCES APP_ROLES(ROLE_ID) ON DELETE CASCADE,

CONSTRAINT UK_QUYEN UNIQUE (ROLE_ID, RESOURCE, ACTION)
);

CREATE TABLE DASHBOARD_LAYOUT_PREFS (
PREF_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
USER_ID NUMBER,
LAYOUT_JSON CLOB,
UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,

CONSTRAINT FK_PREF_USER FOREIGN KEY (USER_ID)
    REFERENCES APP_USERS(USER_ID)
);



