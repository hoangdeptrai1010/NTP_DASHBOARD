);

CREATE TABLE DMLOAIVIENPHI (
IDLOAIVIENPHI NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
IDNHOMVIENPHI NUMBER NOT NULL,
TENLOAIVIENPHI VARCHAR2(255),
CONSTRAINT FK_LOAI_NHOM FOREIGN KEY (IDNHOMVIENPHI)
