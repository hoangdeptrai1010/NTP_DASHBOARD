TONGTIEN NUMBER(18,2),
LOAI_DIEUTRI VARCHAR2(20),
TONG_BHYT_TRA NUMBER(18,2) DEFAULT 0,
TONG_BN_TRA NUMBER(18,2) DEFAULT 0,


